package variable;

public class Var7 {
  public static void main(String[] args) {
    // 코드에서 개발자가 직접 적은 값들을 리터럴 (문자,글자) 라고 말한다.
    int a = 100; // 정수
    double b = 10.6; // 실수
    boolean c = true; // true, false
    char d = 'A'; // 문자하나
    String e = "Hello"; // 문자열

    System.out.println(a);
    System.out.println(b);
    System.out.println(c);
    System.out.println(d);
    System.out.println(e);
  }
}
