package variable;

public class Var5 {
  public static void main(String[] args) {
    // 1. 변수를 선언하고 초기화를 따로 진행
    int a;
    a = 2;
    System.out.println(a);

    // 2. 변수의 선언과 초기화를 한번에 진행
    int b = 2;
    System.out.println(b);

    // 3. 여러 변수의 선언과 초기화
    int c = 3, d = 4;
    System.out.println(c);
    System.out.println(d);
  }
}
